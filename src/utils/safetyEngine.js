  if (query.includes('โคลเบทาซอล') || query.includes('โคลเบทา') || query.includes('clobetasol')) {
    matches.push('clobetasol');
  }
  if (query.includes('เอโซเมพราโซล') || query.includes('เอโซเม') || query.includes('esomeprazole')) {
    matches.push('esomeprazole');
  }
